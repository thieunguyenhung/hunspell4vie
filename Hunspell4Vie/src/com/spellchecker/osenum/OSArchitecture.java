package com.spellchecker.osenum;

public enum OSArchitecture {
	/**
	 * Linux x86-32-bit
	 */
	LINUX_32,
	/**
	 * Linux x86-64-bit
	 */
	LINUX_64,
	/**
	 * Windows x86-32-bit
	 */
	WINDOWS_32,
	/**
	 * Windows x86-64-bit
	 */
	WINDOWS_64,
	/**
	 * Mac OS x86-32-bit (CPU made by Intel)
	 */
	MACOS_32,
	/**
	 * Mac OS x86-64-bit (CPU made by Intel)
	 */
	MACOS_64,
	/**
	 * Mac OS PowerPC 32-bit (CPU made by IBM and Freescale)
	 */
	MACOS_PPC32;
}
